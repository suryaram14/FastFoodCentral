package application;

public class Drinks {
	private double price;
	private int selection;
	
	public Drinks() {
	}
	
	public int getSelection() {
		return selection;
	}

	public void setSelection(int selection) {
		this.selection = selection;
	}
	
	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}
}
